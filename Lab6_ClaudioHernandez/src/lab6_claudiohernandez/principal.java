    public void cargarArchivo() {
        Scanner sc = null;
        File archivo = new File("./clientes.txt");

        if (archivo.exists()) {
            try {
                sc = new Scanner(archivo);
                sc.useDelimiter(";");
                while (sc.hasNext()) {
                    //String nombre, String id, String usuario, String apellido, String contraseña, String nacionalidad, String lugarTrabajo, int dinero
                    sc.nextLine();
                    sc.nextLine();
                    usuarios.add(new usuarios(sc.next(), sc.next(), sc.next(), sc.next(), sc.next(), sc.next(), sc.next(), sc.nextInt()));
                }
            } catch (Exception ex) {
            }
            sc.close();
        }//FIN IF
    }

