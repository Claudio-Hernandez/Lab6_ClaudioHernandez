    public void escribirArchivo() throws IOException {
        FileWriter fw = null;
        File archivo = new File("./clientes.txt");
        BufferedWriter bw = null;
        try {
            fw = new FileWriter(archivo, false);
            bw = new BufferedWriter(fw);
            for (usuarios t : usuarios) {
                //usuario + ";" + contraseña + ";" + nombre + ";" + apellido + ";" + id +";" + nacionalidad +";" + lugarTrabajo + ";" + dinero + ";" + vins+";"
                bw.write(t.getUsuario() + ";");
                bw.write(t.getContraseña() + ";");
                bw.write(t.getNombre() + ";");
                bw.write(t.getApellido() + ";");
                bw.write(t.getId() + ";");
                bw.write(t.getNacionalidad() + ";");
                bw.write(t.getLugarTrabajo() + ";");
                bw.write(t.getDinero() + ";");
                bw.write(t.getVins() + ";");

            }
            bw.flush();
        } catch (Exception ex) {
        }
        bw.close();
        fw.close();
    }
